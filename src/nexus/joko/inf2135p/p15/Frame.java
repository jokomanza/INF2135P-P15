    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
       
        File workbookFile = new File(directory);

        try {
            WritableWorkbook writableWorkbook = Workbook.createWorkbook(workbookFile);
            WritableSheet sheet = writableWorkbook.createSheet("students", 0);
       
            for (int i = 0; i < model.getColumnCount(); i++) {
                sheet.addCell(new Label(i, 0, model.getColumnName(i)));
            }
            for (int row = 0; row < model.getRowCount(); row++) {
                for (int column = 0; column < model.getColumnCount(); column++) {
                    String data = model.getValueAt(row, column).toString();
                    sheet.addCell(new Label(column, row + 1, data));
                }
            }
            writableWorkbook.write();
            writableWorkbook.close();
        } catch (IOException | WriteException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_saveButtonActionPerformed

