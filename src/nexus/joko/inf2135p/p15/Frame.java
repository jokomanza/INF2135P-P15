    private void fetchData() {
        File workbookFile = new File(directory);

        try {
            model.setRowCount(0);

            Workbook workbook = Workbook.getWorkbook(workbookFile);
            Sheet sheet = workbook.getSheet(0);
            Object rowObject[] = new Object[sheet.getColumns()];

            for (int row = 1; row < sheet.getRows(); row++) {
                for (int column = 0; column < sheet.getColumns(); column++) {
                    rowObject[column] = sheet.getCell(column, row).getContents();
                }
                model.addRow(rowObject);
            }

            workbook.close();
        } catch (IOException | IndexOutOfBoundsException | BiffException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }

